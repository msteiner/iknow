package org.ms.iknow.client;

public class Term {

    String term;
    String level;

    /**
     * @return the term
     */
    public String getTerm() {
        return term;
    }

    /**
     * @return the level
     */
    public String getLevel() {
        return level;
    }

    /**
     * @param term the term to set
     */
    public void setTerm(String term) {
        this.term = term;
    }

    /**
     * @param level the level to set
     */
    public void setLevel(String level) {
        this.level = level;
    }
}
